    results = []
    id = kwargs.get("id")
    # Call get_request with a URL parameter
    if id:
        json_result = get_request(url, id=id)
    else:
        json_result = get_request(url)
    if json_result:
        reviews = json_result['data']['docs']
        # For each review object
        for revi in reviews:
            # Create a DealerReview object with values in `doc` object
            review_obj = DealerReview(dealership=revi["dealership"], name=revi["name"], 
                                    purchase=revi["purchase"], review=revi["review"])
            if "id" in revi:
                review_obj.id = revi["id"]
            if "purchase_date" in revi:
                review_obj.purchase_date = revi["purchase_date"]
            if "car_make" in revi:
                review_obj.car_make = revi["car_make"]
            if "car_model" in revi:
                review_obj.car_model = revi["car_model"]
            if "car_year" in revi:
                review_obj.car_year = revi["car_year"]
            review_obj.sentiment = analyze_review_sentiments(review_obj.review)
            results.append(review_obj)

    return results

